#include "../includes/so_long.h"

void	free_map(char **map)
{
	int i;

	i = 0;
	while (map[i])
	{
		free(map[i]);
		i++;
	}
	free(map);
}

void	ft_error(char *str, char **map)
{
	if(map)
		free_map(map);
	ft_printf("%s", str);
	exit(EXIT_FAILURE) ;
}